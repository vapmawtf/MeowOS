#include <stdint.h>
#include <meow/io.h>

void pic_remap()
{
    outb(0x20, 0x11);
    outb(0xA0, 0x11);

    outb(0x21, 0x20);
    outb(0xA1, 0x28);

    outb(0x21, 0x04);
    outb(0xA1, 0x02);

    outb(0x21, 0x01);
    outb(0xA1, 0x01);

    // IRQ0,1,2 enabled
    outb(0x21, 0xF8);
    outb(0xA1, 0xFF);
}